(defn f [n] ; [note: 4clojure.com doesn't like defn!]
  (fn [x]
    (int (Math/pow x n))))

(= 256 ((f 2) 16),
       ((f 8) 2)) ; => true
(= [1 8 27 64] (map (f 3) [1 2 3 4])) ; => true
(= [1 2 4 8 16] (map #((f %) 2) [0 1 2 3 4])) ; => true
